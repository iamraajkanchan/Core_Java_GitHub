package Logics;


public class Pyramids_NIIT {

    public static void main(String[] args) {
      
        int i, j;
        
        for( j = 1; j < 6; j++){
            for(i = 1; i <= j; i++){
                System.out.print("*");
            }
            System.out.println("");
        }
        
    }
    
}
