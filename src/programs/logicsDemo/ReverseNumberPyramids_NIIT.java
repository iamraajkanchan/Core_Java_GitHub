package programs.logicsDemo;

public class ReverseNumberPyramids_NIIT {
    public static void main(String args[]){
        int i, j;
        for(j = 5; j > 0; j--){
            for(i = 1; i <= j; i++){
                System.out.print(i);
            }
            System.out.println("");
        }
    }
}
