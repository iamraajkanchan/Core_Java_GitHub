package programs.logicsDemo;


public class ReversePyramids_NIIT {
    public static void main(String args[]){
        int i, j;
        for(j = 5; j > 0; j --){
            for(i = 1; i <= j; i++){
                System.out.print("*");
            }
            System.out.println("");
        }
        
    }
    
}
